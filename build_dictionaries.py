# Reverse: build per-language dictionary so each lang can be searched
# For each target language, we want: word_in_english -> [translations]
# But we also want to allow searching FROM the target language back to English
# Solution: build a single combined file

combined = {
    "languages": ["es", "it", "fr", "de", "en"],
    "entries": []
}

for english_word, translations in DICTIONARY.items():
    entry = {"en": [english_word]}
    entry.update(translations)
    combined["entries"].append(entry)

with open(os.path.join(OUTPUT_DIR, "dictionary.json"), "w", encoding="utf-8") as f:
    json.dump(combined, f, ensure_ascii=False, indent=2)

print(f"Built dictionary with {len(DICTIONARY)} entries")
